  public String resolve(String key) {
    String resolvedKey = (key == null || key.isBlank()) ? DEFAULT_KEY : key;
    String prompt = PERSONAS.get(resolvedKey);
    if (prompt == null) {
      throw new IllegalArgumentException("Unknown persona key: " + resolvedKey);
    }
    return prompt;
  }
